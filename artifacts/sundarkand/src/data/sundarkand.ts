import { SUNDARKAND_RAW } from "./sundarkandText";

export interface Doha {
  number: string;
  text: string;
}

export interface Section {
  doha: Doha;
  chaupaiPlaceholders: string[];
}

export interface BajrangSection {
  label: string;
  verses: string[];
}

export interface Part {
  id: string;
  title: string;
  subtitle: string;
  durationSec: number;
  accentColor: string;
  headerLines: string[];
  body: PartBody;
}

export type PartBody =
  | { kind: "sundarkand"; sections: Section[] }
  | { kind: "chalisa";    verses: string[] }
  | { kind: "aarti";      verses: string[] }
  | { kind: "bajrangbaan"; sections: BajrangSection[] };

function parseSundarkandSections(): Section[] {
  const lines = SUNDARKAND_RAW
    .split("\n")
    .map(l => l.trim())
    .filter(Boolean);

  const sections: Section[] = [];
  let currentChaupais: string[] = [];
  let i = 0;

  while (i < lines.length) {
    const line = lines[i];

    if (/^॥\s*दोहा\s+\d+/.test(line)) {
      const match = line.match(/दोहा\s+(\d+[कखग]?)/);
      const dohaNum = match ? match[1] : String(sections.length + 1);
      const dohaText = i + 1 < lines.length ? lines[i + 1] : "";
      sections.push({
        doha: { number: dohaNum, text: dohaText },
        chaupaiPlaceholders: [...currentChaupais],
      });
      currentChaupais = [];
      i += 2;
    } else if (
      line.startsWith("॥ चौपाई") ||
      line.startsWith("॥ श्लोक") ||
      line.startsWith("॥ छन्द") ||
      line.startsWith("॥ सोरठा") ||
      (line.startsWith("॥") && !line.includes("दोहा"))
    ) {
      i++;
    } else if (!line.startsWith("॥")) {
      currentChaupais.push(line);
      i++;
    } else {
      i++;
    }
  }

  return sections;
}

